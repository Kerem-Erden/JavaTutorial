
public class Constructors {
	
	int x;//class attribute
	
	// create a class constructor
	//constructor name match the class name
	public Constructors() {
		x = 5;//set the initial value 
	}
	
	
	public static void main(String[] args) {
		//create an object
		Constructors myObject = new Constructors();
		System.out.println(myObject.x);//print the value
	}
	
	
	
	
	
	
	
}	
	